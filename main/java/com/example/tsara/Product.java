package com.example.tsara;

public class Product {
    private int id;
    private String name;
    private String description;
    private double price;

    // Getters
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public double getPrice() {
        return price;
    }
}
